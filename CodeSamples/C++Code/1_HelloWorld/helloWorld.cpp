// Para poder usar ciertas funciones o partes de c++, como la salida (print) o entrada (input) de datos, se requieren archivos
// de encabezado. En este caso, iostream es un archivo que ya es parte de C++ que sirve para
// imprimir en la consola, o para pedir datos al usuario.
#include <iostream>

using namespace std;

// La función main es el punto de entrada de cualquier programa de C++
int main(void)
{
    // Le decimos a la salida estándar, en éste caso la terminal, que vamos a imprimir un texto
    cout << "Hello world";

    // Las funciones regresan un valor en particular
    return 0;
}
